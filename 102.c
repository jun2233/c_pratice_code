#include<stdio.h>
int main() {
    double a, b, c, t;
    double t1 = 0, t2 = 0;
    scanf("%lf%lf%lf%lf", &a, &b, &c, &t);
    if(1 <= a <= 50 && 1 <= b <= 50 && 1 <= c <= 50 && 1 <= t <= 50 && 1 - ((t / a) + (t /  b)) > 0){
        t1 = (1 - t / a - t / b) / ((1 / a) + (1 / b) - (1 / c));
        t2 = t1 + t;
        printf("%0.2lf\n", t2);
    }
}
